function translateObjectsAndPhrases(text) {
  if (!text) return '';
  let result = text;

  phraseReplacements.forEach(item => {
    result = result.replace(item.regex, item.replacement);
  });

  const sortedIngIds = Object.keys(ingredientTranslations).sort((a, b) => b.length - a.length);
  sortedIngIds.forEach(id => {
    const pattern = id.replace(/-/g, '[-\\s]');
    const regex = new RegExp(`\\b${pattern}\\b`, 'gi');
    result = result.replace(regex, ingredientTranslations[id]);
  });

  result = result.replace(/\s+/g, ' ').trim();
  result = translateDigits(result);

  return result;
}

function translateCookingClause(clause) {
  let text = clause.trim();
  if (!text) return '';
  
  const leadingMatch = text.match(/^[^a-zA-Z0-9]+/);
  const trailingMatch = text.match(/[^a-zA-Z0-9]+$/);
  
  const leading = leadingMatch ? leadingMatch[0] : '';
  const trailing = trailingMatch ? trailingMatch[0] : '';
  
  let core = text.slice(leading.length, text.length - (trailingMatch ? trailing.length : 0)).trim();

  if (clauseDictionary[core.toLowerCase()]) {
    return leading + clauseDictionary[core.toLowerCase()] + trailing;
  }

  const words = core.split(/\s+/);
  if (words.length === 0) return clause;

  const firstWord = words[0].toLowerCase();
  let verbEng = '';
  let verbBn = '';

  if (core.toLowerCase().startsWith('bring to a boil')) {
    verbEng = 'bring to a boil';
    verbBn = 'ফুটিয়ে নিন';
  } else if (core.toLowerCase().startsWith('bring to boil')) {
    verbEng = 'bring to boil';
    verbBn = 'ফুটিয়ে নিন';
  } else if (core.toLowerCase().startsWith('let it rest')) {
    verbEng = 'let it rest';
    verbBn = 'রেখে দিন';
  } else if (core.toLowerCase().startsWith('let rest')) {
    verbEng = 'let rest';
    verbBn = 'রেখে দিন';
  } else if (verbs[firstWord]) {
    verbEng = firstWord;
    verbBn = verbs[firstWord];
  }

  if (verbEng) {
    let restOfClause = core.slice(verbEng.length).trim();
    let translatedRest = translateObjectsAndPhrases(restOfClause);

    let finalClause = '';
    if (translatedRest) {
      finalClause = `${translatedRest} ${verbBn}`;
    } else {
      finalClause = verbBn;
    }

    return leading + finalClause + trailing;
  }

  return leading + translateObjectsAndPhrases(core) + trailing;
}

export function translateSentence(text = '') {
  if (!text) return '';
  
  const trimmed = text.trim();

  for (const id in staticRecipeTranslations) {
    const record = staticRecipeTranslations[id];
    if (record.titleBn && record.titleBn.toLowerCase() === trimmed.toLowerCase()) {
      return record.titleBn;
    }
    if (record.descriptionBn && record.descriptionBn.toLowerCase() === trimmed.toLowerCase()) {
      return record.descriptionBn;
    }
    if (record.culturalNoteBn && record.culturalNoteBn.toLowerCase() === trimmed.toLowerCase()) {
      return record.culturalNoteBn;
    }
  }

  for (const id in cuisineTranslations) {
    const record = cuisineTranslations[id];
    if (record.descriptionBn && record.descriptionBn.toLowerCase() === trimmed.toLowerCase()) {
      return record.descriptionBn;
    }
  }

  const parts = trimmed.split(/(\.|;|,?\s+and\s+)/i);
  
  const translatedParts = parts.map(part => {
    if (!part) return '';
    if (part.match(/^(\.|;|,?\s+and\s+)$/i)) {
      if (part.toLowerCase().includes('and')) {
        return ' এবং ';
      }
      return part;
    }
    return translateCookingClause(part);
  });

  return translatedParts.join('').replace(/\s+/g, ' ').trim();
}

export function decorateRecipeTranslations(recipe) {
  if (!recipe) return null;

  const iconic = staticRecipeTranslations[recipe.id] || iconicRecipeTranslations[recipe.id];
  
  const titleBn = iconic?.titleBn || translateSentence(recipe.title);
  const descriptionBn = iconic?.descriptionBn || translateSentence(recipe.description);
  const culturalNoteBn = iconic?.culturalNoteBn || translateSentence(recipe.culturalNote);

  const steps = (recipe.steps || []).map((step, idx) => {
    let instructionBn = '';
    if (iconic?.stepsBn && iconic.stepsBn[idx]) {
      instructionBn = iconic.stepsBn[idx];
    } else {
      instructionBn = translateSentence(step.instruction);
    }
    return {
      ...step,
      instructionBn
    };
  });

  const ingredients = (recipe.ingredients || []).map(ing => {
    return {
      ...ing,
      nameBn: ingredientTranslations[ing.ingredientId] || ing.name || ing.ingredientId
    };
  });

  return {
    ...recipe,
    titleBn,
    descriptionBn,
    culturalNoteBn,
    steps,
    ingredients
  };
}
